"""Alembic environment configuration.

Reads DATABASE_URL from app.config and converts the asyncpg driver
to psycopg2 for Alembic's synchronous migration runner.
"""

from logging.config import fileConfig

from alembic import context
from sqlalchemy import engine_from_config, pool

from app.config import settings
from app.database import Base
from app.models import ChatSession, ChatMessage  # noqa: F401 — register models

config = context.config

if config.config_file_name is not None:
    fileConfig(config.config_file_name)

target_metadata = Base.metadata

# Convert async URL to sync for Alembic
_db_url = settings.DATABASE_URL
_db_url = _db_url.replace("postgresql+asyncpg://", "postgresql://")
# Also handle bare postgresql:// (already correct for sync)
config.set_main_option("sqlalchemy.url", _db_url)

# Tables managed by Prisma — Alembic must never touch these
PRISMA_TABLES = frozenset({
    "users", "accounts", "sessions", "verification_tokens",
    "projects", "integrations", "agent_sessions", "_prisma_migrations",
})


def include_object(object, name, type_, reflected, compare_to):
    """Exclude Prisma-managed tables from autogenerate."""
    if type_ == "table" and name in PRISMA_TABLES:
        return False
    return True


def run_migrations_offline():
    url = config.get_main_option("sqlalchemy.url")
    context.configure(
        url=url,
        target_metadata=target_metadata,
        literal_binds=True,
        dialect_opts={"paramstyle": "named"},
        include_object=include_object,
    )
    with context.begin_transaction():
        context.run_migrations()


def run_migrations_online():
    connectable = engine_from_config(
        config.get_section(config.config_ini_section, {}),
        prefix="sqlalchemy.",
        poolclass=pool.NullPool,
    )
    with connectable.connect() as connection:
        context.configure(
            connection=connection,
            target_metadata=target_metadata,
            include_object=include_object,
        )
        with context.begin_transaction():
            context.run_migrations()


if context.is_offline_mode():
    run_migrations_offline()
else:
    run_migrations_online()
